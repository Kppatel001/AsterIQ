/** Files for a multi-file (framework) project — generated files win, we only add what's missing. */
function buildProjectRepoFiles(opts: {
  name: string;
  projectFiles: Record<string, string>;
  login: string;
  repo: string;
}): { path: string; content: string }[] {
  const { name, projectFiles, login, repo } = opts;
  const year = new Date().getFullYear();
  const out: { path: string; content: string }[] = Object.entries(projectFiles)
    .filter(([p, c]) => p && typeof c === "string")
    .map(([path, content]) => ({ path: path.replace(/^\.?\//, ""), content }));
  const has = (p: string) => out.some((f) => f.path.toLowerCase() === p.toLowerCase());

  if (!has("README.md")) {
    out.push({
      path: "README.md",
      content: `# ${name}\n\nBuilt with **${APP_NAME}** — India's AI no-code platform.\n\n## Run locally\n\n\`\`\`bash\nnpm install\nnpm run dev\n\`\`\`\n\nThen open http://localhost:3000\n\n## Deploy\n\nImport this repository at [vercel.com/new](https://vercel.com/new/import?s=https://github.com/${login}/${repo}) — no configuration needed.\n\n---\n_Generated on ${new Date().toISOString().slice(0, 10)}_\n`,
    });
  }
  if (!has(".gitignore")) {
    out.push({
      path: ".gitignore",
      content: "node_modules/\n.next/\nout/\n.env\n.env*.local\n*.tsbuildinfo\nnext-env.d.ts\n.DS_Store\n",
    });
  }
  out.push({
    path: "LICENSE",
    content: `MIT License\n\nCopyright (c) ${year} ${login}\n\nPermission is hereby granted, free of charge, to any person obtaining a copy of this software and associated documentation files (the "Software"), to deal in the Software without restriction, including without limitation the rights to use, copy, modify, merge, publish, distribute, sublicense, and/or sell copies of the Software, and to permit persons to whom the Software is furnished to do so, subject to the following conditions:\n\nThe above copyright notice and this permission notice shall be included in all copies or substantial portions of the Software.\n\nTHE SOFTWARE IS PROVIDED "AS IS", WITHOUT WARRANTY OF ANY KIND, EXPRESS OR IMPLIED.\n`,
  });
  out.push({
    path: "asteriq.json",
    content: JSON.stringify(
      {
        name,
        generator: `${APP_NAME} AI`,
        type: "framework-project",
        framework: "nextjs",
        repository: `https://github.com/${login}/${repo}`,
        files: out.length,
        deployedAt: new Date().toISOString(),
      },
      null,
      2
    ),
  });
  return out;
}

/** Push all files as ONE atomic commit using the Git Data API. */
async function pushAllFiles(
  token: string,
  login: string,
  repo: string,
  files: { path: string; content: string }[],
  message: string
): Promise<{ ok: boolean; error?: string }> {
  const H = ghHeaders(token);

  // 1. Current main ref (retry — fresh repos can lag)
  let refRes: Response | null = null;
  for (let i = 0; i < 3; i++) {
    refRes = await fetch(`${GH}/repos/${login}/${repo}/git/ref/heads/main`, {
      headers: H,
    });
    if (refRes.ok) break;
    await new Promise((r) => setTimeout(r, 2000));
  }
  if (!refRes || !refRes.ok) {
    return { ok: false, error: "Could not read the repository's main branch." };
  }
  const baseSha = (await refRes.json()).object.sha as string;

  const commitRes = await fetch(
    `${GH}/repos/${login}/${repo}/git/commits/${baseSha}`,
    { headers: H }
  );
  if (!commitRes.ok) return { ok: false, error: "Could not read base commit." };
  const baseTree = (await commitRes.json()).tree.sha as string;

  // 2. New tree with every file
  const treeRes = await fetch(`${GH}/repos/${login}/${repo}/git/trees`, {
    method: "POST",
    headers: H,
    body: JSON.stringify({
      base_tree: baseTree,
      tree: files.map((f) => ({
        path: f.path,
        mode: "100644",
        type: "blob",
        content: f.content,
      })),
    }),
  });
  if (!treeRes.ok) {
    const e = await treeRes.json().catch(() => ({}));
    return { ok: false, error: `Could not create file tree: ${e.message || treeRes.status}` };
  }
  const newTree = (await treeRes.json()).sha as string;

  // 3. Commit
  const newCommitRes = await fetch(`${GH}/repos/${login}/${repo}/git/commits`, {
    method: "POST",
    headers: H,
    body: JSON.stringify({ message, tree: newTree, parents: [baseSha] }),
  });
  if (!newCommitRes.ok) return { ok: false, error: "Could not create commit." };
  const newCommit = (await newCommitRes.json()).sha as string;

  // 4. Move main to the new commit
  const updateRes = await fetch(
    `${GH}/repos/${login}/${repo}/git/refs/heads/main`,
    {
      method: "PATCH",
      headers: H,
      body: JSON.stringify({ sha: newCommit }),
    }
  );
  if (!updateRes.ok) return { ok: false, error: "Could not update main branch." };

  return { ok: true };
}

export async function POST(req: Request) {
  const uid = await verifyFirebaseToken(req.headers.get("authorization"));
  if (!uid) {
    return Response.json({ error: "Unauthorized" }, { status: 401 });
  }

  const { name, code, projectFiles, githubToken, existingRepo } = (await req.json()) as {
    name: string;
    code?: string;
    /** Multi-file project: path -> file contents. Takes priority over `code`. */
    projectFiles?: Record<string, string>;
    githubToken?: string;
    /** Repo name from a previous deploy — reuse it, never create a duplicate */
    existingRepo?: string;
  };
  const isProject =
    !!projectFiles && typeof projectFiles === "object" && Object.keys(projectFiles).length > 0;
  if (!name || (!code && !isProject)) {
    return Response.json({ error: "Missing name or code" }, { status: 400 });
  }

  const token = githubToken;
  if (!token) {
    return Response.json(
      {
        error:
          "Connect your GitHub account first — go to Connections and click Connect GitHub.",
      },
      { status: 400 }
    );
  }

  try {
    // Who is deploying?
    const userRes = await fetch(`${GH}/user`, { headers: ghHeaders(token) });
    if (!userRes.ok) {
      return Response.json(
        {
          error:
            "Your GitHub token is invalid or expired. Go to Connections and reconnect GitHub.",
        },
        { status: 400 }
      );
    }
    const { login } = await userRes.json();

    // Reuse the project's existing repo when it has one — deploys UPDATE, never duplicate.
    const repo =
      existingRepo && /^[A-Za-z0-9._-]+$/.test(existingRepo)
        ? existingRepo
        : `${REPO_PREFIX}-${slugify(name)}`;

    // Create repo if it doesn't exist
    const repoRes = await fetch(`${GH}/repos/${login}/${repo}`, {
      headers: ghHeaders(token),
    });
    if (repoRes.status === 404) {
      const createRes = await fetch(`${GH}/user/repos`, {
        method: "POST",
        headers: ghHeaders(token),
        body: JSON.stringify({
          name: repo,
          description: `Built with ${APP_NAME} AI — ${name}`,
          auto_init: true,
          has_issues: false,
          has_wiki: false,
        }),
      });
      if (!createRes.ok) {
        const err = await createRes.json().catch(() => ({}));
        return Response.json(
          { error: `Could not create repository: ${err.message || createRes.status}` },
          { status: 500 }
        );
      }
      await new Promise((r) => setTimeout(r, 2000));
    }

    // Push the complete project in one commit
    const files = isProject
      ? buildProjectRepoFiles({ name, projectFiles: projectFiles!, login, repo })
      : buildRepoFiles({ name, code: code || "", login, repo });
    const push = await pushAllFiles(
      token,
      login,
      repo,
      files,
      `Deploy from ${APP_NAME} AI — ${new Date().toLocaleString("en-IN")}`
    );
    if (!push.ok) {
      return Response.json({ error: push.error }, { status: 500 });
    }

    // GitHub Pages only serves static files — a framework project needs a build step.
    if (!isProject) {
      await fetch(`${GH}/repos/${login}/${repo}/pages`, {
        method: "POST",
        headers: ghHeaders(token),
        body: JSON.stringify({ source: { branch: "main", path: "/" } }),
      }).catch(() => null);
    }

    return Response.json({
      repoUrl: `https://github.com/${login}/${repo}`,
      pagesUrl: isProject ? null : `https://${login}.github.io/${repo}/`,
      importUrl: isProject
        ? `https://vercel.com/new/import?s=https://github.com/${login}/${repo}`
        : null,
      type: isProject ? "project" : "static",
      repoName: repo,
      filesPushed: files.map((f) => f.path),
    });
  } catch (err) {
    console.error(err);
    return Response.json(
      { error: "Deployment failed. Check your connection and try again." },
      { status: 500 }
    );
  }
}
